#[cfg(test)]
mod tests {
    use super::*;

    fn entry(valid: bool) -> AuditEntry {
        AuditEntry {
            event_id: "evt_001".to_string(),
            rule_name: "GoldSufficientRule".to_string(),
            valid,
            message: if valid {
                None
            } else {
                Some("insufficient gold".to_string())
            },
        }
    }

    #[test]
    fn buffer_grows_until_flush_threshold() {
        let mut log = AuditLog::with_flush_threshold(3);
        log.log(entry(true));
        assert_eq!(log.buffered(), 1);
        log.log(entry(true));
        assert_eq!(log.buffered(), 2);
        // Third entry triggers auto-flush.
        log.log(entry(false));
        assert_eq!(log.buffered(), 0, "buffer should be cleared after auto-flush");
    }

    #[test]
    fn manual_flush_clears_buffer() {
        let mut log = AuditLog::with_flush_threshold(100);
        for _ in 0..10 {
            log.log(entry(true));
        }
        assert_eq!(log.buffered(), 10);
        log.flush();
        assert_eq!(log.buffered(), 0);
    }

    #[test]
    fn flush_on_empty_buffer_is_noop() {
        let mut log = AuditLog::new();
        log.flush(); // should not panic
        assert_eq!(log.buffered(), 0);
    }

    #[test]
    fn drop_flushes_remaining_entries() {
        // Wrap in a block to trigger Drop before assertions.
        let populated = {
            let mut log = AuditLog::with_flush_threshold(1_000);
            for _ in 0..5 {
                log.log(entry(true));
            }
            log.buffered()
        };
        // The value captured before drop should have been 5.
        assert_eq!(populated, 5);
        // After drop, the log is gone — we just verify it didn't panic.
    }
}
